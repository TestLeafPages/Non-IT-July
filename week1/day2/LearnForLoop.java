package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
	//for(intialize the variable,range,increment/dcrement(Varia++ /variable--)	
		for(int i=0;i<=10;i++) {
			//run for 10 rounds..stop when i reach 10th round,increase the count
			System.out.println("Completed round " +i );
		}
		
		System.out.println("*************************" );
		//to decrement from 10 to 0
		for(int i=10;i>=0;i--) {
			//run for 10 rounds..stop when i reach 10th round,increase the count
			System.out.println("Completed round ");
		}
	}

}
